#!/usr/bin/env python3
from __future__ import annotations

import argparse, json, os, pathlib, subprocess, sys
from typing import Any

MODEL_FILES = {
  "8b": "Qwen3-8B-Q4_K_M.gguf",
  "14b": "Qwen3-14B-Q4_K_M.gguf",
  "32b": "Qwen3-32B-Q4_K_M.gguf",
}


from extra.llm_eval_common import load_json, write_json


def _run(cmd:list[str], *, cwd:pathlib.Path, env:dict[str, str], log:pathlib.Path, timeout:float|None) -> None:
  log.parent.mkdir(parents=True, exist_ok=True)
  proc = subprocess.run(cmd, cwd=cwd, env={**os.environ, **env}, text=True,
                        stdout=subprocess.PIPE, stderr=subprocess.STDOUT, timeout=timeout)
  log.write_text(proc.stdout)
  if proc.returncode != 0:
    raise RuntimeError(f"{log}: command failed rc={proc.returncode}\n{proc.stdout[-4000:]}")


def _candidate_rows(loop:dict[str, Any], *, candidate_ids:set[str] | None=None, limit:int | None=None) -> list[dict[str, Any]]:
  rows = [row for row in loop.get("rows", []) if row.get("decision") == "benchmark_next"]
  if candidate_ids is not None:
    rows = [row for row in rows if row.get("id") in candidate_ids]
    missing = sorted(candidate_ids - {row.get("id") for row in rows})
    if missing: raise ValueError(f"requested candidate ids not present with decision=benchmark_next: {missing}")
  if limit is not None: rows = rows[:limit]
  if not rows: raise ValueError("no benchmark_next candidate rows selected")
  return rows


def _current_policy(loop:dict[str, Any]) -> pathlib.Path:
  for row in loop.get("rows", []):
    if row.get("decision") == "baseline":
      policy = row.get("policy_path")
      if policy: return pathlib.Path(policy)
  raise ValueError("loop run missing baseline current policy_path")


def _portable_path(path:pathlib.Path, base:pathlib.Path | None=None) -> str:
  path = path.resolve()
  base = (base or pathlib.Path.cwd()).resolve()
  try:
    return str(path.relative_to(base))
  except ValueError:
    return str(path)


def _decision_row(path:pathlib.Path, candidate:dict[str, Any], *, path_base:pathlib.Path | None=None) -> dict[str, Any]:
  decision = load_json(path / "decision.json")
  return {
    "id": candidate["id"],
    "path": _portable_path(path, path_base),
    "policy": _portable_path(path / "policy.json", path_base),
    "status": decision.get("status"),
    "gain": decision.get("gain"),
    "reference_tok_s": (decision.get("explicit") or {}).get("avg_tok_s"),
    "candidate_tok_s": (decision.get("generated") or {}).get("avg_tok_s"),
    "reference_stable": (decision.get("explicit") or {}).get("stable"),
    "candidate_stable": (decision.get("generated") or {}).get("stable"),
    "ab_match": decision.get("ab_match"),
    "reasons": decision.get("reasons", []),
    "changes": candidate.get("changes", []),
  }


def build_matrix(model:str, loop:dict[str, Any], rows:list[dict[str, Any]], out:pathlib.Path,
                 *, path_base:pathlib.Path | None=None) -> dict[str, Any]:
  decisions = [_decision_row(out / row["id"], row, path_base=path_base) for row in rows]
  accepted = [row for row in decisions if row["status"] == "accept"]
  bad_accepts = [row["id"] for row in accepted if row["gain"] is None]
  if bad_accepts:
    raise ValueError(f"{out}: accept rows require numeric gain, got gain=None for {bad_accepts}")
  ties = [row for row in decisions if row["status"] == "tie"]
  rejected = [row for row in decisions if row["status"] == "reject"]
  invalid = [row for row in decisions if row["status"] == "invalid"]
  needs_rerun = [row for row in decisions if row["status"] == "needs-rerun"]
  best = max(accepted, key=lambda row: row["gain"]) if accepted else None
  return {
    "kind": "qk_loop_candidate_matrix",
    "model": model.upper(),
    "source_loop": loop.get("source_candidates"),
    "rows": decisions,
    "summary": {
      "candidates": len(decisions),
      "accepted": len(accepted),
      "ties": len(ties),
      "rejected": len(rejected),
      "invalid": len(invalid),
      "needs_rerun": len(needs_rerun),
      "best_id": None if best is None else best["id"],
      "best_gain": None if best is None else best["gain"],
      "next_decision": "promote_best_candidate" if best else "descriptor_knob_frontier_exhausted",
    },
  }


def matrix_markdown(matrix:dict[str, Any]) -> str:
  lines = [
    f"# QK Loop Candidate Matrix: {matrix['model']}",
    "",
    "Candidate policies generated by the Ansor-transition static loop and",
    "benchmarked against the current accepted generated policy, not against",
    "explicit primitive flags.",
    "",
    "## Summary",
    "",
  ]
  summary = matrix["summary"]
  lines += [
    f"- candidates: `{summary['candidates']}`",
    f"- accepted: `{summary['accepted']}`",
    f"- ties: `{summary['ties']}`",
    f"- rejected: `{summary['rejected']}`",
    f"- invalid: `{summary['invalid']}`",
    f"- needs rerun: `{summary['needs_rerun']}`",
    f"- best id: `{summary['best_id']}`",
    f"- best gain: `{summary['best_gain']}`",
    f"- next decision: `{summary['next_decision']}`",
    "",
    "| id | status | gain % | reference tok/s | candidate tok/s | A/B | reasons |",
    "|---|---|---:|---:|---:|---:|---|",
  ]
  for row in matrix["rows"]:
    gain_pct = None if row["gain"] is None else row["gain"] * 100
    reasons = "; ".join(row.get("reasons") or []) or "none"
    lines.append(
      f"| `{row['id']}` | `{row['status']}` | "
      f"{'n/a' if gain_pct is None else f'{gain_pct:.2f}'} | "
      f"{'n/a' if row['reference_tok_s'] is None else f'{row['reference_tok_s']:.2f}'} | "
      f"{'n/a' if row['candidate_tok_s'] is None else f'{row['candidate_tok_s']:.2f}'} | "
      f"`{row['ab_match']}` | {reasons} |"
    )
  lines.append("")
  return "\n".join(lines)


def run_model(args, model:str) -> dict[str, Any]:
  loop_path = args.transition_dir / "search" / model / "run.json"
  loop = load_json(loop_path)
  selected = _candidate_rows(loop, candidate_ids=set(args.candidate_id) if args.candidate_id else None, limit=args.limit)
  current_policy = (_current_policy(loop)).resolve()
  model_path = (args.model_root / MODEL_FILES[model]).expanduser().resolve()
  out = args.out / model
  out.mkdir(parents=True, exist_ok=True)
  for row in selected:
    cand_out = out / row["id"]
    cmd = [
      sys.executable, "extra/qk_policy_pipeline.py",
      "--model", str(model_path),
      "--out", str(cand_out),
      "--device", args.device,
      "--level", "2",
      "--iters", "2",
      "--benchmark", str(args.benchmark),
      "--reference-mode", "policy",
      "--reference-policy", str(current_policy),
      "--input-policy", str(pathlib.Path(row["policy_path"]).resolve()),
      "--repeats", str(args.repeats),
      "--max-extra-repeats", str(args.max_extra_repeats),
      "--ab-tokens", str(args.ab_tokens),
      "--profile", args.profile,
      "--accept-gain", str(args.accept_gain),
      "--tie-band", str(args.tie_band),
      "--decode-timeout", str(args.decode_timeout),
      "--ab-timeout", str(args.ab_timeout),
    ]
    if args.reuse: cmd.append("--reuse")
    if args.force: cmd.append("--force")
    _run(cmd, cwd=args.repo, env={"PYTHONPATH": ".", "QK_PRIMITIVE_STORAGE": "shared"},
         log=cand_out / "pipeline-driver.log", timeout=args.pipeline_timeout)
  matrix = build_matrix(model, loop, selected, out, path_base=args.repo)
  write_json(out / "matrix.json", matrix)
  (out / "matrix.md").write_text(matrix_markdown(matrix))
  return matrix


def main() -> int:
  parser = argparse.ArgumentParser(description="Benchmark QK Ansor-transition loop candidates against current generated policy")
  parser.add_argument("--transition-dir", type=pathlib.Path, default=pathlib.Path("bench/qk-ansor-transition-20260612"))
  parser.add_argument("--out", type=pathlib.Path, default=pathlib.Path("bench/qk-ansor-transition-20260612/benchmarks"))
  parser.add_argument("--repo", type=pathlib.Path, default=pathlib.Path.cwd())
  parser.add_argument("--model-root", type=pathlib.Path, default=pathlib.Path("~/models"))
  parser.add_argument("--models", nargs="+", choices=tuple(MODEL_FILES), default=("8b", "14b", "32b"))
  parser.add_argument("--candidate-id", action="append", help="specific candidate id to run; repeatable")
  parser.add_argument("--limit", type=int, help="run only the first N benchmark_next candidates per model")
  parser.add_argument("--device", default="AMD")
  parser.add_argument("--benchmark", type=int, default=128)
  parser.add_argument("--repeats", type=int, default=3)
  parser.add_argument("--max-extra-repeats", type=int, default=4)
  parser.add_argument("--ab-tokens", type=int, default=32)
  parser.add_argument("--profile", choices=("auto", "always", "never"), default="never")
  parser.add_argument("--accept-gain", type=float, default=0.03)
  parser.add_argument("--tie-band", type=float, default=0.03)
  parser.add_argument("--decode-timeout", type=float, default=1800)
  parser.add_argument("--ab-timeout", type=float, default=1800)
  parser.add_argument("--pipeline-timeout", type=float)
  parser.add_argument("--reuse", action="store_true")
  parser.add_argument("--force", action="store_true")
  args = parser.parse_args()
  args.repo = args.repo.resolve()
  args.transition_dir = args.transition_dir.resolve()
  args.out = args.out.resolve()
  matrices = [run_model(args, model) for model in args.models]
  summary = {
    "kind": "qk_loop_benchmark_summary",
    "models": [m["model"] for m in matrices],
    "rows": [{"model": m["model"], **m["summary"]} for m in matrices],
  }
  write_json(args.out / "summary.json", summary)
  lines = ["# QK Loop Benchmark Summary", "", "| model | candidates | accepted | ties | rejected | best | next decision |",
           "|---|---:|---:|---:|---:|---|---|"]
  for row in summary["rows"]:
    lines.append(f"| `{row['model']}` | {row['candidates']} | {row['accepted']} | {row['ties']} | "
                 f"{row['rejected']} | `{row['best_id']}` | `{row['next_decision']}` |")
  lines.append("")
  (args.out / "summary.md").write_text("\n".join(lines))
  print(json.dumps(summary, indent=2, sort_keys=True))
  return 0


if __name__ == "__main__":
  raise SystemExit(main())
